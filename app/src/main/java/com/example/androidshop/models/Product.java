package com.example.androidshop.models;

public class Product {
    String description;
    String name;
    String id;
    String imageUrl;
    String price;
    String type;

    public Product() {
    }

    public String getDescription() {
        return description;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }
}
